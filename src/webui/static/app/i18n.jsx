// ── Global state (mirrors useDataVersion pattern) ─────────────────────
window.__i18nLang = "en";
window.__i18nVersion = 0;
var __i18nSubscribers = new Set();

// ── Translation function ──────────────────────────────────────────────
function t(key, params) {
  var lang = window.__i18nLang || "en";
  var dict = TRANSLATIONS[lang] || TRANSLATIONS["en"];
  var text = dict[key];
  if (text === undefined) {
    text = (TRANSLATIONS["en"] || {})[key];
    if (text === undefined) {
      console.warn("i18n: missing key \"" + key + "\" for lang \"" + lang + "\"");
      return key;
    }
  }
  if (params) {
    for (var p in params) {
      if (Object.prototype.hasOwnProperty.call(params, p)) {
        text = text.split("{" + p + "}").join(String(params[p]));
      }
    }
  }
  return text;
}

// ── Language setter ───────────────────────────────────────────────────
function setLang(lang) {
  if (lang !== "en" && lang !== "zh") return;
  window.__i18nLang = lang;
  try { localStorage.setItem("cyrene-lang", lang); } catch (e) { /* quota */ }
  window.__i18nVersion = (window.__i18nVersion || 0) + 1;
  __i18nSubscribers.forEach(function (fn) { fn(window.__i18nVersion); });
  // Update <html lang> attribute
  document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";
}

// ── React hook ────────────────────────────────────────────────────────
function useI18n() {
  var _useState = React.useState;
  var _useEffect = React.useEffect;
  var vState = _useState(window.__i18nVersion || 0);
  var v = vState[0];
  var setV = vState[1];
  _useEffect(function () {
    __i18nSubscribers.add(setV);
    return function () { __i18nSubscribers.delete(setV); };
  }, []);
  return {
    t: t,
    lang: window.__i18nLang || "en",
    setLang: setLang,
  };
}

// ── Auto-detect language ──────────────────────────────────────────────
(function initLang() {
  var stored;
  try { stored = localStorage.getItem("cyrene-lang"); } catch (e) {}
  if (stored === "en" || stored === "zh") {
    window.__i18nLang = stored;
  } else {
    var nav = (navigator.language || "").toLowerCase();
    window.__i18nLang = nav.startsWith("zh") ? "zh" : "en";
  }
  document.documentElement.lang = window.__i18nLang === "zh" ? "zh-CN" : "en";
  window.__i18nVersion = 0;
})();

